package com.example.custommathlib;

public class CalculateSum {

    /**
     * Adds two numbers together and returns the result
     * @param firstNumber The first number to add
     * @param secondNumber The second number to add
     * @return The result of the given numbers added
     */
    public int calculateSum(int firstNumber, int secondNumber){

        return firstNumber + secondNumber;
    }
}
